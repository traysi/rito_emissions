int main(int argc, char *argv[]) {
  // Loop through and show them all.

  if (argv[1]) {
    int bsize;
    long nHeight = strtol(argv[1], NULL, 10);
    bsize = GetBlockEmission(nHeight);
    cout << nHeight << ":" << bsize << endl;
  } else {
    int bsize;
    long total = 0;
    for(int a = 0; a < 52560000; a=a+1) {
       bsize = GetBlockEmission(a);
       total = total + bsize;
       cout << a << ":" << bsize << ":" << total << endl;
    }
  }
}


